    private void consultarFornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarFornActionPerformed
        conexaoBD cb = new conexaoBD();
        FornecedorDAO forn = null;
        
        
         if(!cnpj.getText().equals(cnpj.getValue())){
            JOptionPane.showMessageDialog(null,"Campo não pode ser vazio!","Erro",JOptionPane.ERROR_MESSAGE,null);
        }else{

            try{
                forn = cb.consultarFornecedor(cnpj.getText());
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"CPNJ não cadastrado","Erro",JOptionPane.ERROR_MESSAGE,null);
            }

            if(forn.getCnpj()==null){
               JOptionPane.showMessageDialog(null,"CPNJ não cadastrado","Erro",JOptionPane.ERROR_MESSAGE,null);
            }else{
            JOptionPane.showMessageDialog(null,"Encontrado com sucesso!");

            conexaoBD bd = new conexaoBD();
            
            cnpj.setText(forn.getCnpj());
            nomeForn.setText(forn.getNome());
            ruaForn.setText(forn.getRua());
            bairroForn.setText(forn.getBairro());
            numeroForn.setText(forn.getNum());
            cepForn.setText(forn.getCep());
            tel1Forn.setText(forn.getTelefone1());
            tel2Forn.setText(forn.getTelefone2());
            razaoSocForn.setText(forn.getRazao());
            inscEst.setText(forn.getInscEstadual());
            cidadeForn.setText(forn.getFCidade());
            estadoForn.setText(forn.getFEstado().toString());
            }
         }
        
    }//GEN-LAST:event_consultarFornActionPerformed

