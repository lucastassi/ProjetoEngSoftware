    private void excluirFornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirFornActionPerformed
        conexaoBD cb = new conexaoBD();
        FornecedorDAO forn = null;

         
        try{
             
        if((cnpj.getText().equals(cnpj.getValue()))){ //Verifica se o campo está preenchido
           
                   
             int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o fornecedor?","Atenção",JOptionPane.YES_NO_OPTION);
             
             if(resposta == JOptionPane.YES_OPTION){
                cb.excluirFornecedor(cnpj.getText());
                        cnpj.setText("");
                        nomeForn.setText("");
                        ruaForn.setText("");
                        bairroForn.setText("");
                        numeroForn.setText("");
                        cepForn.setText("");
                        tel1Forn.setText("");
                        tel2Forn.setText("");
                        razaoSocForn.setText("");
                        inscEst.setText("");
                        cidadeForn.setText("");
                        estadoForn.setText("");
                JOptionPane.showMessageDialog(null, "Fornecedor excluído com sucesso!");
             }else{
                 if(resposta == JOptionPane.NO_OPTION){
                     //Somente fecha a caixa de pergunta.
                 }
             }
         }else{
            
         if((!cnpj.getText().equals(cnpj.getValue()))){
            JOptionPane.showMessageDialog(null, "Campo CNPJ vazio!","Erro ao excluir",JOptionPane.ERROR_MESSAGE,null);
            }
        }
            }
         catch(SQLException ex){
             JOptionPane.showMessageDialog(null, "Erro na exclusão do fornecedor!","Atenção",JOptionPane.ERROR_MESSAGE,null);
         }
    }//GEN-LAST:event_excluirFornActionPerformed

    private void alterarFornActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarFornActionPerformed
        String nome = nomeForn.getText();
        String razao = razaoSocForn.getText();
        String estadual = inscEst.getText();
        String cnpj1 = cnpj.getText();
        String rua = ruaForn.getText();
        String bairro = bairroForn.getText();
        String numero = numeroForn.getText();
        String cep = cepForn.getText();
        String cidade = cidadeForn.getText();
        String estado = estadoForn.getText();
        String tel1 = tel1Forn.getText();
        String tel2 = tel2Forn.getText();
        
        validaEntrada ve = new validaEntrada();
            /*Verifica se há campos obrig. vazios. Se não tiver, faz a inserção no BD.*/
            FornecedorDAO fornec = new FornecedorDAO(cnpj1,estadual,nome,razao,rua,bairro,cep,numero,cidade,estado,tel1,tel2);
       

                try{
                    fornec.atualizaFornecedor();      
                    JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
                }catch(entradaInvalidaException ex){
                    JOptionPane.showMessageDialog(null,""+ex.toString().substring(85,ex.toString().length()),"Erro",JOptionPane.ERROR_MESSAGE,null);
                }
        
    }//GEN-LAST:event_alterarFornActionPerformed

