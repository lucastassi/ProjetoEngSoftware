    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel modelo1 = (DefaultTableModel) tabelaCarrinho.getModel();
        
        if(tabelaCarrinho.getSelectedRow() >=0){
            precoFinal -= Float.parseFloat(modelo1.getValueAt(tabelaCarrinho.getSelectedRow(), 3).toString().replace(",", "."));
            modelo1.removeRow(tabelaCarrinho.getSelectedRow());
            tabelaCarrinho.setModel(modelo1);
        }else{
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
        
        precoFinalCompra.setText("R$ "+formatter.format(precoFinal));
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        precoFinal = 0;
        
        DefaultTableModel modelo1 = (DefaultTableModel) tabelaCarrinho.getModel();
        DefaultTableModel modelo = (DefaultTableModel) tabelaConsulta.getModel();
        
        int[] index = tabelaConsulta.getSelectedRows();
        Object[] row = new Object[5];
        
        String aux;
   
    try{
        
        for(int i=0; i<index.length; i++){
            row[0] = modelo.getValueAt(index[i], 0);
            row[1] = modelo.getValueAt(index[i], 1);
            row[2] = quantidade.getText();
            row[3] = formatter.format(precoProduto() * Float.parseFloat(quantidade.getText()));
            
            modelo1.addRow(row);
        }
        
        for(int i=0; i < modelo1.getRowCount(); i++){ //Calcula o preço final da compra.
            aux = modelo1.getValueAt(i, 3).toString().replace(",", ".");
            System.out.println("PRECO TOTAL: "+aux);
            precoFinal += Float.parseFloat(aux);
        }
        
        quantidade.setText("");
        
        precoFinalCompra.setText("R$ "+formatter.format(precoFinal));
        
    }catch(NumberFormatException ex){
        JOptionPane.showMessageDialog(null, "Campo 'Quantidade' não pode ser vazio!","Atenção",JOptionPane.ERROR_MESSAGE,null);
    }
    }//GEN-LAST:event_jButton4ActionPerformed

