    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        ArrayList<ProdutoDAO> p = new ArrayList<ProdutoDAO>();
            conexaoBD cb = new conexaoBD();
            
            try{
                
                if(comboOpcao.getSelectedItem().equals("Descrição")){
                    p = (ArrayList<ProdutoDAO>) cb.consultaTodosProdutosPorNome(pesquisaGeral.getText());
                    System.out.println("ASD: "+pesquisaGeral.getText());
                }else{
                    if(comboOpcao.getSelectedItem().equals("Código")){
                        p = (ArrayList<ProdutoDAO>) cb.consultaTodosProdutosPorCodigo(produtoCodigo.getText());
                        System.out.println("ASD: "+pesquisaGeral.getText());
                    }
                }
            } catch (SQLException ex) {
            
                Logger.getLogger(carrinhoCompras.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if(p.isEmpty()){
                JOptionPane.showMessageDialog(null, "Banco de Dados Vazio!");
                return;
            }
            
            DefaultTableModel modelo = (DefaultTableModel) tabelaConsulta.getModel();
            
            modelo.setNumRows(0);
            
            Object[] fila = new Object[modelo.getColumnCount()];
            for(int i=0; i < p.size(); i++){
                modelo.addRow(new Object[]{
                    p.get(i).getCodigo(),
                    p.get(i).getNome(),
                    p.get(i).getDescricaoReduzida(),
                    p.get(i).getFabricante(),
                    p.get(i).getCusto(),
                    p.get(i).getTributos()
                });
                
            }
    }//GEN-LAST:event_jButton3ActionPerformed

