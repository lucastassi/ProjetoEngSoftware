    //Pega o CPF na Tabela
    public String info(){
        int linha = tabelaConsulta.getSelectedRow();
        String info = tabelaConsulta.getModel().getValueAt(linha, 0).toString();
        
        return info;
    }
    
    public String Custo(){
        int linha = tabelaConsulta.getSelectedRow();
        String info = tabelaConsulta.getModel().getValueAt(linha, 4).toString();
        
        return info;
    }
    
    public String Tributos(){
        int linha = tabelaConsulta.getSelectedRow();
        String info = tabelaConsulta.getModel().getValueAt(linha, 5).toString();
        
        return info;
    }
    
    public float precoProduto(){
        custo = Float.parseFloat(Custo());
        tributos = Float.parseFloat(Tributos()); //Captura os valores de custo e tributo para imprimir para o usuário.
        
        precoProduto = custo + (custo*(tributos/100)); // Faz o calculo do proço final.
        
        return precoProduto;
    }

    
