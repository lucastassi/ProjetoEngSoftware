    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            ArrayList<ProdutoDAO> p = new ArrayList<ProdutoDAO>();
            conexaoBD cb = new conexaoBD();
            
            try{
                p = (ArrayList<ProdutoDAO>) cb.consultaTodosProdutos();
            } catch (SQLException ex) {
            
                Logger.getLogger(ConsultaProdutos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if(p.isEmpty()){
                JOptionPane.showMessageDialog(null, "Banco de Dados Vazio!");
                return;
            }
            
            DefaultTableModel modelo = (DefaultTableModel) tabelaConsulta.getModel();
            
            modelo.setNumRows(0);
            
            Object[] fila = new Object[modelo.getColumnCount()];
            for(int i=0; i < p.size(); i++){
                modelo.addRow(new Object[]{
                    p.get(i).getCodigo(),
                    p.get(i).getNome(),
                    p.get(i).getDescricaoReduzida(),
                    p.get(i).getFabricante(),
                    p.get(i).getCusto(),
                    p.get(i).getTributos()
                });
                
            }
            
    }//GEN-LAST:event_jButton1ActionPerformed

