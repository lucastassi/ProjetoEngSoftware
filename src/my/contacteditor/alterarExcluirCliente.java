    private void alterarClienteActionPerformed(java.awt.event.ActionEvent evt) {                                            
        String nome = nomeCliente.getText();
        String razao = razaoSocCliente.getText();
        String estadual = inscEstCliente.getText();
        String cnpj1 = cnpjCliente.getText();
        String rua = ruaCliente.getText();
        String bairro = bairroCliente.getText();
        String numero = numeroCliente.getText();
        String cep = cepCliente.getText();
        String cidade = cidadeCliente.getText();
        String estado = estadoCliente.getText();
        String tel1 = tel1Cliente.getText();
        String tel2 = tel2Cliente.getText();
        
        validaEntrada ve = new validaEntrada();
            /*Verifica se há campos obrig. vazios. Se não tiver, faz a inserção no BD.*/
            ClienteDAO cli = new ClienteDAO(cnpj1,estadual,nome,razao,rua,bairro,cep,numero,cidade,estado,tel1,tel2);
       

                try{
                    cli.atualizaCliente();      
                    JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
                }catch(entradaInvalidaException ex){
                    JOptionPane.showMessageDialog(null,""+ex.toString().substring(85,ex.toString().length()),"Erro",JOptionPane.ERROR_MESSAGE,null);
                }
        
    }                                           

