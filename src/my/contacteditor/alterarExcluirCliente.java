    private void consultarClienteActionPerformed(java.awt.event.ActionEvent evt) {                                              
        conexaoBD cb = new conexaoBD();
        ClienteDAO cliente = null;
        
        
         if(!cnpjCliente.getText().equals(cnpjCliente.getValue())){
            JOptionPane.showMessageDialog(null,"Campo não pode ser vazio!","Erro",JOptionPane.ERROR_MESSAGE,null);
        }else{

            try{
                cliente = cb.consultarCliente(cnpjCliente.getText());
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"CPNJ não cadastrado","Erro",JOptionPane.ERROR_MESSAGE,null);
            }

            if(cliente.getCnpj()==null){
               JOptionPane.showMessageDialog(null,"CPNJ não cadastrado","Erro",JOptionPane.ERROR_MESSAGE,null);
            }else{
            JOptionPane.showMessageDialog(null,"Encontrado com sucesso!");

            conexaoBD bd = new conexaoBD();
            
            cnpjCliente.setText(cliente.getCnpj());
            nomeCliente.setText(cliente.getNome());
            ruaCliente.setText(cliente.getRua());
            bairroCliente.setText(cliente.getBairro());
            numeroCliente.setText(cliente.getNum());
            cepCliente.setText(cliente.getCep());
            tel1Cliente.setText(cliente.getTelefone1());
            tel2Cliente.setText(cliente.getTelefone2());
            razaoSocCliente.setText(cliente.getRazao());
            inscEstCliente.setText(cliente.getInscEstadual());
            cidadeCliente.setText(cliente.getCCidade());
            estadoCliente.setText(cliente.getCEstado().toString());
            }
         }
        
    }                                             

